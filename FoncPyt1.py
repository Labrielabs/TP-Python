def somme(a, b):
    """
    Calcule la somme de deux nombres.
    
    Args:
        a (float): Premier nombre.
        b (float): Deuxième nombre.
    
    Returns:
        float: Somme des deux nombres.
    """
    return a + b

# Exemple d'utilisation
nombre1 = 5
nombre2 = 3
resultat = somme(nombre1, nombre2)
print(f"La somme de {nombre1} et {nombre2} est égale à {resultat}.")
